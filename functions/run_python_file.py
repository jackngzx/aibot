import os
import subprocess
from google.genai import types

schema_run_python_file = types.FunctionDeclaration(
    name="run_python_file",
    description="Check if the file that was pointed is a python file, and if yes, run the python file",
    parameters=types.Schema(
        type=types.Type.OBJECT,
        properties={
            "file_path": types.Schema(
                type=types.Type.STRING,
                description="Path to the file in the permitted working directory",
            ),
            "args": types.Schema(
                type=types.Type.ARRAY,
                items=types.Schema(
                    type=types.Type.STRING,
                    description="Optional command on top of running python file",
                ),
            ),
        },
        required=["file_path"],
    ),
)


def run_python_file(working_directory, file_path, args=None):
    working_dir_abs = os.path.abspath(working_directory)
    target_file = os.path.normpath(os.path.join(working_dir_abs, file_path))
    valid_target_file = os.path.commonpath([working_dir_abs, target_file])
    if valid_target_file != working_dir_abs:
        return f'Error: Cannot execute "{file_path}" as it is outside the permitted working directory'
    if not os.path.isfile(target_file):
        return f'Error: "{file_path}" does not exist or is not a regular file'
    if not target_file.endswith(".py"):
        return f'Error: "{file_path}" is not a Python file'

    command = ["python", target_file]
    if args is not None:
        command.extend(args)
    result = subprocess.run(command, capture_output=True, timeout=30, text=True)
    output_string = ""
    if result.returncode != 0:
        output_string += f"Process exited with code {result.returncode}"
    if not result.stdout and not result.stderr:
        output_string += "No output produced."
    output_string += f"STDOUT: {result.stdout}\nSTDERR: {result.stderr}"

    return output_string
